/* can_task function */
void can_task(void const * argument)
{

  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
    message.Data[0] = 0x11;
    message.Data[1] = 0x22;
    message.Data[2] = 0x33;
    message.Data[3] = 0x44;
    message.Data[4] = 0x55;
    message.Data[5] = 0x66;
    message.Data[6] = 0x77;
    message.Data[7] = 0x88;
    
    message.IDE = CAN_ID_STD;
    message.StdId = 0x10;
    
    
    hcan.pTxMsg = &message;
    
    HAL_CAN_Transmit(&hcan, 5);
    osDelay(100);
  }
  /* USER CODE END 5 */ 
}

